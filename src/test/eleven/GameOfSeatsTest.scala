package eleven

import org.scalatest.funsuite.AnyFunSuite

class GameOfSeatsTest extends AnyFunSuite {
  test("game of seats") {
    assert(GameOfSeats.countOccupied("L.LL.LL.LL\nLLLLLLL.LL\nL.L.L..L..\nLLLL.LL.LL\nL.LL.LL.LL\nL.LLLLL.LL\n..L.L.....\nLLLLLLLLLL\nL.LLLLLL.L\nL.LLLLL.LL") === 37)
  }

  test("game of seats 2") {
    assert(GameOfSeats.countOccupiedAll("L.LL.LL.LL\nLLLLLLL.LL\nL.L.L..L..\nLLLL.LL.LL\nL.LL.LL.LL\nL.LLLLL.LL\n..L.L.....\nLLLLLLLLLL\nL.LLLLLL.L\nL.LLLLL.LL") === 26)
  }

  test("game of seats iterations") {
    var map = GameOfSeats.parseMap("L.LL.LL.LL\nLLLLLLL.LL\nL.L.L..L..\nLLLL.LL.LL\nL.LL.LL.LL\nL.LLLLL.LL\n..L.L.....\nLLLLLLLLLL\nL.LLLLLL.L\nL.LLLLL.LL")
    map = GameOfSeats.iterate(map)
    assert(GameOfSeats.printMap(9, 9, map) == "#.##.##.##\n#######.##\n#.#.#..#..\n####.##.##\n#.##.##.##\n#.#####.##\n..#.#.....\n##########\n#.######.#\n#.#####.##\n")
    println()
    map = GameOfSeats.iterate(map)
    assert(GameOfSeats.printMap(9, 9, map) == "#.LL.L#.##\n#LLLLLL.L#\nL.L.L..L..\n#LLL.LL.L#\n#.LL.LL.LL\n#.LLLL#.##\n..L.L.....\n#LLLLLLLL#\n#.LLLLLL.L\n#.#LLLL.##\n")
    println()
    map = GameOfSeats.iterate(map)
    assert(GameOfSeats.printMap(9, 9, map) == "#.##.L#.##\n#L###LL.L#\nL.#.#..#..\n#L##.##.L#\n#.##.LL.LL\n#.###L#.##\n..#.#.....\n#L######L#\n#.LL###L.L\n#.#L###.##\n")
    println()
    map = GameOfSeats.iterate(map)
    assert(GameOfSeats.printMap(9, 9, map) == "#.#L.L#.##\n#LLL#LL.L#\nL.L.L..#..\n#LLL.##.L#\n#.LL.LL.LL\n#.LL#L#.##\n..L.L.....\n#L#LLLL#L#\n#.LLLLLL.L\n#.#L#L#.##\n")
    println()
    map = GameOfSeats.iterate(map)
    assert(GameOfSeats.printMap(9, 9, map) == "#.#L.L#.##\n#LLL#LL.L#\nL.#.L..#..\n#L##.##.L#\n#.#L.LL.LL\n#.#L#L#.##\n..L.L.....\n#L#L##L#L#\n#.LLLLLL.L\n#.#L#L#.##\n")
  }


}
